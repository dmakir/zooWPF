namespace MVVMZoo.ViewModels.Bird
{
    public abstract class Bird
    {
        protected float _flightAltitude;
        protected Bird(float flightAltitude)
        {
            _flightAltitude = flightAltitude;
        }

        public float Height()
        {
            return Height();
        }

        public float Weight()
        {
            return Weight();
        }

        public string EyeColor()
        {
            return EyeColor();
        }

        public string Sound()
        {
            return Sound();
        }
    }
}